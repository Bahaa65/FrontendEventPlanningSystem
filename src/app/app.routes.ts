import { Routes } from '@angular/router';
import { LoginComponent } from './components/login/login.component';
import { SignupComponent } from './components/signup/signup.component';
import { DashboardComponent } from './components/dashboard/dashboard';
import { CreateEventComponent } from './components/create-event/create-event';
import { EventDetailsPageComponent } from './components/event-details-page/event-details-page';
import { AuthGuard } from './guards/auth.guard';

export const routes: Routes = [
  { path: '', redirectTo: '/login', pathMatch: 'full' },
  { path: 'login', component: LoginComponent },
  { path: 'signup', component: SignupComponent },
  { path: 'dashboard', component: DashboardComponent, canActivate: [AuthGuard] },
  { path: 'events/create', component: CreateEventComponent, canActivate: [AuthGuard] },
  { path: 'events/:id', component: EventDetailsPageComponent, canActivate: [AuthGuard] },
  { path: '**', redirectTo: '/login' }
];
